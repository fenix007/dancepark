     */
    private $id;

    /**
     * @var
     *
     * @ORM\ManyToOne(targetEntity="DancePark\DancerBundle\Entity\Dancer", inversedBy="danceType", cascade={"merge", "persist"})
     */
    private $dancer;

    /**
     * @var DanceType
     *
     * @ORM\ManyToOne(targetEntity="DancePark\CommonBundle\Entity\DanceType", inversedBy="id", cascade={"merge", "persist"})
     */
