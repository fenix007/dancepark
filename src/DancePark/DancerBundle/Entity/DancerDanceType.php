    /**
     * @var
     *
     * @ORM\Column(name="is_pro", type="boolean")
     */
    private $isPro;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set dancer
     *
     * @param integer $dancer
     * @return DancerDanceType
     */
